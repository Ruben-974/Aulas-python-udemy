"""
Tipos de dados:

    str = string:
        'exemplo' "exemplo"

    int = inteiro:
        4 65 -87 0 -1

    float = real/ponto flutuante:
        -10.4 9.7 0.0 -4.55 1.01

    bool = booleano/lógico:
        True / False 10 == 10
"""

# str: nome
print('Rúben', type('Rúben'))

# int: idade
print(17, type(17))

# float: altura
print(1.72, type(1.72))

# bool: mairo de idade
print(17 > 18, type(17 > 18))
